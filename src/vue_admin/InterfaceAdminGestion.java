    /**
     * Fonction utilitaire pour palier aux problèmes de rafraichissement
     * @param panelDeBase
     * @param newPanel
     */
    public void swapPanel(JComponent panelDeBase, JComponent newPanel){
        panelDeBase.removeAll();
        panelDeBase.add(newPanel);
        revalidate();
        repaint();
    }

